void program1() {
	int n, i, k, l;
	double sum;
	cout << "1. Программа по заданному массиву размера N и целым числам K и L (1<=K<=L<=N) найдёт среднее арифметическое элементов массива с номерами от K до L включительно (отчёт начинается с нуля).\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	double* a = new double[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1; i < n; i++) cin >> a[i];
	cout << "Задайте число K: ";
	cin >> k;
	cout << "Задайте число L: ";
	cin >> l;

	sum = 0;
	for (i = k; i <= l; i++) sum += a[i];

	cout << "Вывод: среднее арифметическое элементов массива с номерами от K до L включительно равно " << sum / (l - k + 1) << ".\n\n";

	delete[] a;
}


void program2() {
	int i, b, flag, n;
	cout << "2. Программа получает на вход целочисленный массив размера N, не содержащий одинаковых чисел, и проверяет, образуют ли его элементы арифметическую прогрессию" <<
		"(элементы рассматриваются в том порядке, в котором они вводились, то есть не рассматривается возможность перестроить порядок элементов для получения прогрессии).\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	int* a = new int[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1; i < n; i++) cin >> a[i];

	if (n == 1) {
		cout << "Заданный массив состоит из одного элемента, поэтому такая числовая последовательность арифметическую прогрессию не образует.\n" <<
			"Вывод: 0.\n\n";
		delete[] a;
		return;
	}

	flag = 0;
	b = abs(a[1] - a[0]);
	for (i = 0; i < n - 1; i++) {
		if (abs(a[i] - a[i + 1]) != b) {
			flag = 1;
			break;
		}
	}

	if (flag == 1) {
		cout << "Элементы заданного массива не образуют арифметическую прогрессию.\n" <<
			"Вывод: 0.\n\n";
	}
	else {
		cout << "Элементы заданного массива образуют арифметическую прогрессию.\n" <<
			"Вывод: " << b << ".\n\n";
	}

	delete[] a;
}


