void program4() {
	int i, max, n;
	cout << "4. Программа получает на вход массив размера N и находит номер его последнего локального максимума (первый и последний элементы также рассматриваются, хоть у них лишь по одному соседнему элементу; отчёт начинается с нуля).\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	double* a = new double[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1; i < n; i++) cin >> a[i];

	if (n == 1) {
		cout << "Заданный массив имеет лишь один элемент, поэтому номер единственного локального максимума равен 0.\n\n";
		delete[] a;
		return;
	}

	if (a[0] > a[1]) max = 0;

	for (i = 1; i < n - 1; i++) {
		if (a[i] > a[i - 1] && a[i] > a[i + 1]) {
			max = i;
		}
	}

	if (a[n - 1] > a[n - 2]) max = i;

	cout << "Вывод: номер последнего локального миксимума равен " << max << ".\n\n";

	delete[] a;
}


void program5() {
	int n, i, j, flag;
	cout << "5. Программа получает на вход целочисленный массив размера N, содержащий ровно два одинаковых элемента, находит номера этих одинаковых элементов и выводит эти номера в порядке возрастания (отчёт элементов в массиве начинается с нуля).\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	int* a = new int[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1; i < n; i++) cin >> a[i];

	flag = 0;
	for (i = 0; i < n - 1; i++) {
		for (j = i + 1; j < n; j++) {
			if (a[i] == a[j]) {
				flag = 1;
				break;
			}
		}
		if (flag == 1) break;
	}

	cout << "Вывод: номера одинаковых элементов в порядке возрастания: " << i << ", " << j << ".\n\n";

	delete[] a;
}