void program3() {
	int i, n, lastOdd;
	cout << "3. Программа получает на вход целочисленный массив размера N, увеличивает все нечётные числа содержащиеся в массиве на исходное значение последнего нечётного числа. Если нечётные числа в массиве отсутствуют, то массив оставляется без изменений.\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	int* a = new int[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1; i < n; i++) cin >> a[i];

	lastOdd = 0;
	for (i = n - 1;i >= 0; i--) {
		if (a[i] % 2 == 1) {
			lastOdd = a[i];
			break;
		}
	}

	if (lastOdd != 0) {
		for (i = 0;i < n;i++) {
			if (a[i] % 2 == 1) {
				a[i] += lastOdd;
			}
		}
		cout << "Вывод: преобразованный массив: ";
	}
	else {
		cout << "Вывод: в заданном массиве нечётных чисел нет, поэтому массив остался без изменений: ";
	}

	for (i = 0;i < n - 1;i++) cout << a[i] << ", ";
	cout << a[i] << ".\n\n";

	delete[] a;
}

void program4() {
	int n, i, min, max;
	cout << "4. Программа получает на вход массив размера N и обнуляет элементы массива, расположенные между его минимальным и максимальным элементами (не включая минимальный и максимальный элементы).\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	double* a = new double[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1;i < n;i++) cin >> a[i];


	max = 0;
	min = 0;
	for (i = 0; i < n; i++) {
		if (a[i] >= a[max]) max = i;
		if (a[i] <= a[min]) min = i;
	}

	if (max != min) {
		if (min < max) {
			for (i = min + 1;i < max; i++) a[i] = 0;
		}
		else {
			for (i = max + 1;i < min; i++) a[i] = 0;
		}
	}

	cout << "Вывод: полученный массив: ";
	for (i = 0;i < n - 1;i++) cout << a[i] << ", ";
	cout << a[i] << ".\n\n";

	delete[] a;
}

void program5() {
	int n, i, j;
	double buf;
	cout << "5. Программа получает на вход массив размера N, все элементы которого, кроме первого, упорядочены по возрастанию, и помещает первый элемент на новую позицию, делая тем самым массив упорядоченным.\n";
	cout << "Задайте размер массива N: ";
	cin >> n;
	double* a = new double[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1;i < n;i++) cin >> a[i];

	for (i = 1;i < n - 1;i++) {
		if (a[0] >= a[i] && a[0] <= a[i + 1]) {
			buf = a[0];
			for (j = 0; j < i; j++) {
				a[j] = a[j + 1];
			}
			a[i] = buf;
		}
	}

	cout << "Вывод: полученный массив: ";
	for (i = 0;i < n - 1;i++) cout << a[i] << ", ";
	cout << a[i] << ".\n\n";

	delete[] a;
}
