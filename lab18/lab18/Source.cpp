	cout << "Задайте размер массива N: ";
	cin >> n;
	double* a = new double[n];
	double* b = new double[n];
	cout << "Задайте элементы массива, разделяя их пробелами: ";
	cin >> a[0];
	for (i = 1;i < n;i++) cin >> a[i];

	if (n == 1) {
		cout << "Вывод: заданный массив имеет лишь один элемент, поэтому в нем нет элемента с индексом \"1\".\n\n";
		return;
	}

	b[0] = (a[0] + a[1]) / 2; // не хочется усложнять цикл, идущий ниже, поэтому вынесу несложное вычисление b[0] за тело цикла

	for (i = 1; i < n; i++) {
		b[i] = 0;
		for (j = 1; j <= i; j++) {
			b[i] += a[j];
		}
		b[i] /= j - 1;
	}

	cout << "Вывод: новый массив B: ";
	for (i = 0; i < n - 1;i++) cout << b[i] << ", ";
	cout << b[i] << ".\n\n";

